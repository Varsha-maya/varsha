public class str2 {
    public static void main(String args[]) {
        String S1 = new String("Hi I am Varsha");
        System.out.println("Original String is :\n\n " + S1);
        System.out.println("\nString after replacing word: \n\n" + S1.replace("Varsha", "maya"));
        System.out.println("\nString after replacing letter:\n\n " + S1.replace('a', 'A'));

    }
}